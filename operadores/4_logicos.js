//Os operadores lógicos comparam dois valores booleanos e retorna um valor true ou false.

true && false // false
false || false // false
!true // false

//É comum usarmos estes operadores lógicos, para combinar operações relacionais

1 == '1' || "a" == "a" // true
35 === '35' && 1 == 10 // false
!( 'a' == 1) // true